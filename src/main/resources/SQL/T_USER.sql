	USER_ID	VARCHAR(20) PRIMARY KEY,
	ACCOUNT VARCHAR(20) NOT NULL COMMENT 'account',
	psd VARCHAR(20) NOT NULL COMMENT 'password',
	INS_DATE DATETIME COMMENT 'Insert Time',
	INS_USER_ID VARCHAR(20) COMMENT 'Insert User',
